import fs from 'node:fs'
import path from 'node:path'

const TARGET_WORDS = Number(process.env.WIKTIONARY_TARGET_WORDS || 1200)
const MIN_WORD_LENGTH = 3
const MAX_WORD_LENGTH = 11
const API_URL = 'https://es.wiktionary.org/w/api.php'

const normalizeWord = (value) => {
  return value
    .normalize('NFD')
    .replace(/[\u0300-\u036f]/g, '')
    .toUpperCase()
    .replace(/[^A-Z]/g, '')
}

const isUsefulWord = (value) => {
  return value.length >= MIN_WORD_LENGTH && value.length <= MAX_WORD_LENGTH
}

const fetchTitlesBatch = async (continueToken) => {
  const url = new URL(API_URL)
  url.searchParams.set('action', 'query')
  url.searchParams.set('format', 'json')
  url.searchParams.set('list', 'allpages')
  url.searchParams.set('apnamespace', '0')
  url.searchParams.set('aplimit', '500')
  url.searchParams.set('apfilterredir', 'nonredirects')
  if (continueToken) {
    url.searchParams.set('apcontinue', continueToken)
  }

  const response = await fetch(url)
  if (!response.ok) {
    throw new Error(`Wiktionary API error: HTTP ${response.status}`)
  }

  const data = await response.json()
  const pages = data?.query?.allpages ?? []
  const nextToken = data?.continue?.apcontinue

  return {
    titles: pages.map((page) => page.title),
    nextToken,
  }
}

const run = async () => {
  const unique = new Map()
  let continueToken

  while (unique.size < TARGET_WORDS) {
    const { titles, nextToken } = await fetchTitlesBatch(continueToken)

    if (!titles.length) {
      break
    }

    for (const title of titles) {
      const normalized = normalizeWord(title)
      if (!isUsefulWord(normalized)) {
        continue
      }

      if (!unique.has(normalized)) {
        unique.set(normalized, {
          word: normalized,
          clue: 'Termino de Wikcionario ES',
        })
      }

      if (unique.size >= TARGET_WORDS) {
        break
      }
    }

    if (!nextToken) {
      break
    }

    continueToken = nextToken
  }

  const words = [...unique.values()].sort((a, b) => {
    if (a.word.length !== b.word.length) {
      return a.word.length - b.word.length
    }
    return a.word.localeCompare(b.word)
  })

  const output = `export type WiktionaryWordEntry = {\n  word: string\n  clue: string\n}\n\n// Generated by scripts/sync-wiktionary-es.mjs\n// Source: https://es.wiktionary.org (titles normalized to A-Z).\nexport const WIKTIONARY_ES_WORDS: WiktionaryWordEntry[] = ${JSON.stringify(words, null, 2)}\n`

  const outputPath = path.resolve(process.cwd(), 'src/lib/wiktionaryWords.ts')
  fs.writeFileSync(outputPath, output, 'utf8')

  console.log(`Wrote ${words.length} Wiktionary ES words to src/lib/wiktionaryWords.ts`)
}

run().catch((error) => {
  console.error('Failed to sync Wiktionary ES words')
  console.error(error)
  process.exit(1)
})
